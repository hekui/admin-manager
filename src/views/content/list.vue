<template>
  <div class="content-container">
    <section class="form">
      <el-form ref="form" :inline="true" :model="filter">
        <el-form-item label="文章标题：">
          <el-input v-model="filter.title" placeholder="请输入名称orID" :clearable="true"></el-input>
        </el-form-item>
        <el-form-item label="公众号名称：">
          <el-input v-model="filter.wechatName" placeholder="请输入名称or微信号" :clearable="true"></el-input>
        </el-form-item>
        <el-form-item label="文章类型：" class="article-type-label">
          <el-cascader
            :options="articleTypeDict"
            :change-on-select="true"
            @change="contentTypeChange"
            :clearable="true">
          </el-cascader>
        </el-form-item>
        <el-form-item label="公众号类型：">
          <el-cascader
            :options="paccountTypeDict"
            :change-on-select="true"
            @change="wechatTypeChange"
            :clearable="true">
          </el-cascader>
        </el-form-item>
        <el-form-item label="发布时间：">
          <el-date-picker
            v-model="releaseTime"
            type="datetimerange"
            align="right"
            unlink-panels
            range-separator="至"
            start-placeholder="开始日期"
            end-placeholder="结束日期"
            :clearable="true"
            value-format="yyyy-MM-dd HH:mm:ss"
            :picker-options="pickerOptions"
            @change="releaseTimeChange" >
          </el-date-picker>
        </el-form-item>
        <el-form-item>
          <el-button icon="el-icon-search" @click="submitFilter">搜索</el-button>
        </el-form-item>
      </el-form>
    </section>
    <section class="table">
      <el-table
        :data="listData.list"
        border
        v-loading="loading"
        style="width: 100%">
        <el-table-column
          type="index"
          label="序号"
          width="50"
          :index="getIndex">
        </el-table-column>
        <el-table-column
          prop="wechatName"
          label="公众号名称">
        </el-table-column>
        <el-table-column
          prop="city"
          label="城市"
          width="80">
        </el-table-column>
        <el-table-column
          prop="title"
          label="文章标题"
          width="180">
        </el-table-column>
        <el-table-column
          label="状态"
          width="50">
          <template slot-scope="scope">
            <span>{{statusFilter(scope.row.status)}}</span>
          </template>
        </el-table-column>
        <el-table-column
          label="发布时间">
          <template slot-scope="scope">
            <span>{{releaseTimeFilter(scope.row.releaseTime)}}</span>
          </template>
        </el-table-column>
        <el-table-column
          label="类型">
          <template slot-scope="scope">
            <span>{{scope.row.typeName || "-"}}</span>
          </template>
        </el-table-column>
        <el-table-column
          prop="readNum"
          label="阅读量"
          width="90">
        </el-table-column>
        <el-table-column
          prop="likeNum"
          label="点赞量"
          width="80">
        </el-table-column>
        <el-table-column
          prop="wordsNum"
          label="字数"
          width="90">
        </el-table-column>
        <el-table-column
          fixed="right"
          label="操作"
          width="100">
          <template slot-scope="scope">
            <div>
              <el-button @click="handleDetail(scope.row)" type="text" size="small">详情</el-button>
            </div>
            <div>
              <el-button @click="handleEdit(scope.row)" type="text" size="small">二次编辑</el-button>
            </div>
            <div>
              <el-button v-if="scope.row.status === 1 || scope.row.status === 2" style="color: red;" @click="handleEnable(scope.row)" type="text" size="small">{{scope.row.status === 1 ? '锁定' : scope.row.status === 2 ? '启用' : '' }}</el-button>
            </div>
          </template>
        </el-table-column>
      </el-table>
    </section>
    <section class="pagination">
      <el-pagination
        background
        @size-change="handleSizeChange"
        @current-change="handleCurrentChange"
        :current-page="page.pageNo"
        :page-sizes="[10, 20, 30, 50]"
        :page-size="page.pageSize"
        layout="total, sizes, prev, pager, next, jumper"
        :total="listData.totalPage">
      </el-pagination>
    </section>
  </div>
</template>

<script>
import { mapState, mapGetters } from 'vuex'
import { parseTime, pickerOptions } from '@/utils'

export default {
  name: 'contentlist',
  data() {
    return {
      loading: false,
      filter: {
        title: '', // 文章标题
        wechatName: '', // 公众号名称
        contentTypeId: '', // 文章类型
        wechatTypeId: '', // 公众号类型
        beginDate: '', // 时间段查询-起（精确到分）
        endDate: '' // 时间段查询-止（精确到分）
      },
      releaseTime: [],
      pickerOptions,
      page: {
        pageNo: 1,
        pageSize: 20
      }
    }
  },
  computed: {
    ...mapState({
      listData: state => state.content.listData
    }),
    ...mapGetters(['paccountTypeDict', 'articleTypeDict'])
  },
  created() {
    this.$store.dispatch('getTypeDict', { cityId: this.$store.state.cityId, code: 1 }) // 查询公众号类型
    this.$store.dispatch('getTypeDict', { cityId: this.$store.state.cityId, code: 3 }) // 查询文章类型
    this.fetchData()
  },
  methods: {
    fetchData() {
      this.loading = true
      this.$store.dispatch('getContentList', Object.assign({}, this.filter, this.page)).then(() => {
        this.loading = false
      }).catch(() => {
        this.loading = false
      })
    },
    statusFilter(status) {
      return status === 0 ? '删除' : status === 1 ? '启用' : status === 1 ? '锁定' : ''
    },
    // 文章类型改变触发
    contentTypeChange(value) {
      const types = [...value]
      this.filter.contentTypeId = types.pop()
    },
    // 公众号类型改变触发
    wechatTypeChange(value) {
      const types = [...value]
      this.filter.wechatTypeId = types.pop()
    },
    // 发布时间改变触发
    releaseTimeChange(value) {
      value = value || ['', '']
      this.filter.beginDate = value[0]
      this.filter.endDate = value[1]
    },
    releaseTimeFilter(time) {
      return parseTime(Number(time))
    },
    // 获取序号
    getIndex(index) {
      return (this.page.pageNo - 1) * this.page.pageSize + index + 1
    },
    // 改变每页条数
    handleSizeChange(val) {
      this.page.pageSize = val
      this.fetchData()
    },
    // 改变当前页
    handleCurrentChange(val) {
      this.page.pageNo = val
      this.fetchData()
    },
    // 条件查询
    submitFilter() {
      this.page.pageNo = 1
      this.fetchData()
    },
    // 查看详情
    handleDetail(data) {
      this.$router.push({ path: '/content/detail', query: { id: data.id }})
    },
    // 二次编辑
    handleEdit(data) {
      this.$router.push({ path: '/content/edit', query: { id: data.id }})
    },
    // 禁用/启用
    handleEnable(data) {
      let tips = '是否启用?'
      if (data.status === 1) tips = '是否禁用?'
      this.$confirm(tips, '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        this.loading = true
        this.$store.dispatch('changeContentStatus', data).then(() => {
          this.loading = false
          this.fetchData()
          this.$message({
            type: 'success',
            message: '操作成功!'
          })
        }).catch(() => {
          this.loading = false
          this.$message({
            message: '操作失败！',
            type: 'error'
          })
        })
      }).catch(() => {
        this.$message({
          type: 'info',
          message: '已取消操作!'
        })
      })
    }
  }
}
</script>

